saldo = 450
saque = 200

print(saldo == saque)
# >> False (Igual)

print(saldo != saque)
# >> True (Diferente)

print(saldo > saque)
# >> True (Maior)

print(saldo >= saque)
# >> True (Maior ou igual)

print(saldo < saque)
# >> False (Menor)

print(saldo <= saque)
# >> False (Menor ou igual)